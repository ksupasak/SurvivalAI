// ─── Current locale state ────────────────────────────────────────────────────

let currentLocale: SupportedLocale = 'en';
let listeners: Array<() => void> = [];

export function getLocale(): SupportedLocale {
  return currentLocale;
}

export function setLocale(locale: SupportedLocale): void {
  currentLocale = locale;
  // Persist locale across restarts (fire-and-forget)
  setSetting('locale', locale).catch(() => {});
  for (const listener of listeners) {
    listener();
  }
}

/**
 * Load persisted locale on app startup.
 * Call once in your root layout.
 */
export async function initLocale(): Promise<void> {
  try {
    const saved = await getSetting('locale');
    if (saved && SUPPORTED_LOCALES.some((l) => l.code === saved)) {
      currentLocale = saved as SupportedLocale;
      for (const listener of listeners) {
        listener();
      }
    }
  } catch {
    // Keep default English
  }
}

function subscribe(listener: () => void): () => void {
  listeners.push(listener);
  return () => {
    listeners = listeners.filter((l) => l !== listener);
  };
}

/**
 * Translate a key to the current locale.
 * Falls back to English if translation is missing.
 */
export function t(key: string): string {
  const entry = translations[key];
  if (!entry) return key;
  return entry[currentLocale] || entry.en || key;
}

/**
 * Translate with parameter substitution.
 * Use {param} placeholders in translation strings.
 * Example: tFormat('greeting', { name: 'Alex' }) → "Hello Alex"
 */
export function tFormat(key: string, params: Record<string, string | number>): string {
  let str = t(key);
  for (const [k, v] of Object.entries(params)) {
    str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
  }
  return str;
}

/**
 * Translate a supply category name from English to current locale.
 */
export function getCategoryName(englishName: string): string {
  const map: Record<string, string> = {
    'Water': 'cat_water',
    'Food': 'cat_food',
    'First Aid': 'cat_first_aid',
    'Hygiene': 'cat_hygiene',
    'Shelter': 'cat_shelter',
    'Tools': 'cat_tools',
  };
  const key = map[englishName];
  return key ? t(key) : englishName;
}

/**
 * React hook that triggers re-render when locale changes.
 */
export function useLocale(): SupportedLocale {
  const [locale, setLocaleState] = useState<SupportedLocale>(currentLocale);

  useEffect(() => {
    const unsubscribe = subscribe(() => {
      setLocaleState(currentLocale);
    });
    return unsubscribe;
  }, []);

  return locale;
}
